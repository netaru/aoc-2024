struct machine {
    double ax, ay, bx, by, xtarget, ytarget;

    machine(double _ax,
            double _ay,
            double _bx,
            double _by,
            double _xtarget,
            double _ytarget)
        : ax(_ax), ay(_ay), bx(_bx), by(_by), xtarget(_xtarget), ytarget(_ytarget) {}

    int64_t score() {
        int64_t y = (ytarget - (ay * (xtarget / ax))) / (by - (ay * (bx / ax)));
        int64_t x = (xtarget - (bx * y)) / ax;
        int64_t d = x + 1;
        for (int64_t i = x - 10; i < d; i++) {
            x = ax * i;
            y = ay * i;
            if ((x > xtarget) || (y > ytarget)) break;
            if ((x == xtarget) && (y == ytarget)) { return i * 3; }
            if ((!((int64_t) (xtarget - x) % (int64_t) bx)) && (!((int64_t) (ytarget - y) % (int64_t) by)) &&
                (((xtarget - x) / bx) == ((ytarget - y) / by))) {
                return i * 3 + ((xtarget - x) / bx);
            }
        }
        return 0;
    }
};

